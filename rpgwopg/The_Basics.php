      <tr>
        <td width="525" height="100%" valign="top" style="padding: 4; text-indent: 10">
          Here are some of the basics to playing RPG World Online.
          <hr height="1" noshade size="1" color="#000080">
		  <center><A NAME="Attributes"><b>Attributes</B></a></center>
          <ul>
          <li>Attributes are general abilities of player
          <li>May be raised by spending experience
          <li>To raise: click up arrow button next to attribute
          <li>Attributes affect a skill's base value, therefore raise an attribute and you will raise any skill that uses the attribute
          <li>Life: Basic hit points. When life reaches 0 you die.
          <li>Stamina: Energy for doing things, see Stamina later in Help
          <li>Mana: Energy for doing magic
          <li>Strength: Ability to hold, carry, wear, etc.
          <li>Dexterity: Hand-eye coordination, balance
          <li>Quickness: How fast one is
          <li>Intelligence: Ability to focus the mind
          <li>Wisdom: Mental capacity
          <li>Clicking on the name will give XP needed for next raise

          <hr>
          <center><A NAME="Skills"><b>Skills</B></a></center>
          <li>Skills are specific abilities of player
          <li>Skills will be improved with usage
          <li>Skill's base value is a formula, for example Melee Defense Base = (Dexterity + Quickness) / 3
          <li>Clicking on a skill name will give details and XP needed until next raise

          <hr>
          <center><A NAME="Player Status Bars"><b>Player Status Bars</B></a></center>
          <li>In the upper/middle part of screen are 3 bars: GREEN, YELLOW, and BLUE
          <li>GREEN is players current Life
          <li>YELLOW is players current Stamina
          <li>BLUE is players current Mana
          <li>The bar will go to the left as your values go down
          <li>Click on the bars to toggle the display mode for them
          <hr>
          <center><A NAME="Movement"><b>Movement</B></a></center>
          <li>Use keypad or arrow keys to move single steps
          <li>Right click on map for player to "go-to" there
          <li>Tactical map is in upper-left
          <li>Strategic map is under "Map" in tab MFD
          <li>NumLock may need to be on
          <li>/GOTO [x], [y]

          <hr>
          <center><A NAME="Fighting"><b>Fighting</B></a></center>
          <li>Click on a target in the tactical display
          <li>Select "A" button in lower right of screen
          <li>Your player will move within range and start attacking
          <li>If the target moves out of your view, attacking is cancelled
          <li>Move the player or click "A" again to cancel attacking
          <li>Critical attack is when an attack is 3 times better then the defend
          <li>Clicking "G" will automatically attack anything in range

          <hr>
          <center><A NAME="Death"><b>Death</B></a></center>
          <li>When your Life reaches 0, you die
          <li>When you die, you get a 3% reduction in Vitae (see Vitae section)
          <li>You lose half the gold you carry and an item you are carrying
          <li>Normally you will auto-resurrect at the last Life Stone you attuned to
          <li>If you don't, exit world and re-enter world with the same player

          <hr>
          <center><A NAME="Vitae"><b>Vitae</B></a></center>
          <li>Normal vitae is 100%
          <li>A player loses 3% of their vitae upon death
          <li>Vitae is restored when you earn experience
          <li>Vitae only affects skills, not attributes for example: when vitae is 95%, then all skills reduced by 5%
          <li>Your players current Vitae is indicated by the "dot" in the upper right corner, just above the MFD tabs
          <li>The Vitae Dot will be different colors to indicate varying levels of Vitae
          <li>GREEN: 100% or Normal
          <li>YELLOW: 95% to 99%
          <li>ORANGE: 85% to 94%
          <li>RED: less than 85%
          <li>Click on Vitae Dot to see exact Vitae in the lower right Text display

          <hr>
          <center><A NAME="Chat"><b>Chat</B></a></center>
          <li>Normal chat is NOT global, NOT all players get the message
          <li>Normal chat messages have about 1-2 screen range
          <li>TAB key "should" activate it
          <li>Type your message
          <li>Numeric keypad still controls player
          <li>Press ENTER send message
          <li>"Menu" tab has option to save chat to "chat.log"
          <li>Commands can be preceded by @, /, or \
          <li>The follow commands do different chat functions:
          <li>@, /, Or \ - alone gives list
          <li>/PLAYER or /WHO  - Shows all players currently in the world
          <li>/KILLS - Shows all players you have killed
          <li>/DEATHS - Shows the number of times you have died
          <li>/AGE - Shows your age in V-Time
          <li>/DOB - Shows when you were born
          <li>/TELL [name], [message] or /T [name], [message] - Sends a private message to a player
          <li>/RETELL [message] or /RT [message] - Sends a private message to the last person you messaged
          <li>/REPLY [message] or /R [message] - Sends a private message back to the last person who messaged you
          <li>/IGNORE [name] - Add or remove a player from your ignore list
          <li>/IGNORE - View your ignore list
          <li>/IGNOREGLOBAL - Stop receiving global messages
          <li>/IGNORETELL - Stop receiving private messages
          <li>/GLOBAL [message] or /G [message] - Send a message to everyone online
          <li>/GUILDCHAT [message] or /Z [message] - Send a message to all your guild members online

          <hr>
          <center><A NAME="Item Use"><b>Item Use</B></a></center>
          <li>Left click/drag items to move them
          <li>Left double click on map item to view it
          <li>Right click on items in inventory to get popup menu
          <li>Drag item in your inventory on top another to merge
          <li>Right click on items with a quantity, gives "split" option

          <hr>
          <center><A NAME="Food"><b>Food</B></a></center>
          <li>Right click on a food item in inventory, select Eat
          <li>Players can eat and store up to 250 units of food
          <li>About 144 units of food used each V-day, or once per 10 V-minutes
          <li>That gives over 3 V-days of going without food
          <li>When food is zero, you start to starve losing Stamina then Life
          <li>Check the bottom of the "Player" tab for food levels

          <hr>
          <center><A NAME="Magic System"><b>Magic System</B></a></center>
          <li>To cast spells you need a wand readied in your hand. Just drag it to the "weapon" slot
          <li>Wands each have a power level that magnifies a spells intensity, so a wand of power 2 does 2 times the effect as a wand of power 1
          <li>Wands wear out with use and will degrade and eventually break which may cause damage to the caster
          <li>The appropriate skill is need and used to determine the caster ability to successfully cast a spell
          <li>The higher a magic skill is the higher level wand they can use successfully
          <li>Black Magic is for offensive spells
          <li>White Magic is for defensive and healing spells
          <li>Red Magic is for Enchantment spells
          <li>Blue Magic is for Item spells
          <li>Spells do not do critical hits
          <li>Runes are the formula for creating and casting spells. You need the right runes, in the right order, in order to cast any specific spell. Each rune has a specific purpose and they are not just random for each spell. For example: the spell Heal Self I use 4 runes (Nal, Oyo, Alq, Yag), the first rune is always the "Target" rune. For this spell, Nal means Self. The others you must figure out your self.
          <li>When researching new spells, that is the time to determine the order. During casting the order of runes is not needed. Also, a minimum of skill is needed to learn the spell, so if a combo does not work when you are at a low skill level, it might work at higher skill levels.
          <li>/SPELLCOUNT - Shows how many spells there are and their names but not rune combinations

          <hr>
          <center><A NAME="Land Ownership"><b>Land Ownership</B></a></center>
          <li>Land parcels are 20x20 in size
          <li>Each player may own 1 land parcel
          <li>The following chat commands apply:
          <li>/LANDCLAIM - Claims the land as your own
          <li>/LANDUNCLAIM - Unclaims the land you own. You must be on the land for it to work
          <li>/LANDLIST - Shows all your land parcels that you own
          <li>/LANDOWNER - Tells you who owns the land you are on
          <li>/LANDGIVE [name] - Transfers ownership of the land to [name]. The receiving player must be online
          <li>/LANDRIGHTS [name] - Gives or revokes rights to other. If no name is specified it lists players with rights to the land.
          <li>/SURFACE [optional number] - Changes the appearance of a flat tile in your land

          <hr>
          <center><A NAME="Mail"><b>Mail</B></a></center>
          <li>Mail system is designed for simple offline communication, in game
          <li>All date/times are in game world units.
          <li>Commands:
          <li>/MAIL - Lists all mail in your mail box
          <li>/MAIL [number] - Lists a single mail
          <li>/MAILSEND [name], [message] - Sends mail to a player
          <li>/MAILDEL [number] - Deletes the mail number
	    <li>/MAILDEL * - Deletes all the mail
          <li>/MAILNEW - Lists all NEW mail only
          <li>Once a NEW mail is listed, it is assumed viewed and it is no longer marked as NEW

          <hr>
          <center><A NAME="Perks"><b>Perks</B></a></center>
          <li>The following are the perks for a player that has them enabled
          <li>2 land claims instead of 1
          <li>Skill cap of 400 is lifted
          <li>Weapons with a rating of or over 120 usable
          <li>Armor with AL of or over 15 usable
          <li>Wands with power level of or over 2.0 usable
          <li>All spells are usable

          <hr>
          </ul>
