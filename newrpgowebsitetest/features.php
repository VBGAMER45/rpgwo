     <p>RPG World Online (RPGWO) is an online, persistent, massively
multi-player(50-100?), role playing game. It is written entirely in Visual Basic
and uses API for graphics and Direct Sound, well, for sound. My motto or driving
goal is to create a simulation type world with RPG gameplay, thus: "Where
simulation meets gameplay!" It is a dynamic world. Maps can be changed and
all items and such can be made and/or destroyed. An empty world
in which players can build it the way they want! </P>
<P>The world will have a plot but it will be secondary. Most RPGs are combat
oriented. I want it to even with other features. For example, a blacksmith and a
swordsman of equal level playing for 1 hour should earn the same amount of
experience.</p>

