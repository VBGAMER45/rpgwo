      <p>
Combat (of course)<br>
Player improvement and development based on skills system <br>
Magic (gotta have it) <br>
Chat system (including offline messages)<br>
Land ownership <br>
Weather (rain, snow, drought, etc)<br>
Water flows (like to lower elevations and floods too!)<br>
Nature (plants and animals grow (and die!)) <br>
Trade skills (blacksmith, farm, mining, tinker, jeweler, etc)<br>
Day/night effects with lighting <br>
Dynamic plot (players affect outcome)<br>
Events (like sacking a city with monsters!)<br></p>

